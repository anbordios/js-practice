let firstFavNumb = 5;
let secondFavNumb = 3;

console.log(firstFavNumb > secondFavNumb); // true, because 5 is greater than 3
console.log(firstFavNumb < secondFavNumb); // false, because 5 is not less than 3
console.log(firstFavNumb >= secondFavNumb); // true, because 5 is greater than 3
console.log(firstFavNumb <= secondFavNumb); // false, because 5 is not less than or equal to 3
console.log(firstFavNumb === secondFavNumb); // false, both numbers are not the same
console.log(firstFavNumb == secondFavNumb); // false, because the number 3 is converted to string
console.log(firstFavNumb !== secondFavNumb); //true, because both values are not equal
console.log(firstFavNumb != secondFavNumb); // true, because after conversion,both values are not the same